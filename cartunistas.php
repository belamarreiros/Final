                <p>
                    Um cartunista, também conhecido como cartoonista ou humorista gráfico, é um artista visionário que
                    se especializa em cartum, uma forma de arte que combina humor e crítica para retratar aspectos do
                    dia a dia de uma sociedade. Este trabalho é muitas vezes criado para entretenimento, comentário
                    político, ou publicidade. Além disso, muitos cartunistas expandem seu talento para o mundo da
                    animação, trazendo suas histórias em quadrinhos para a vida em movimento.
                </p>
                <p>
                    Os cartunistas são verdadeiros camaleões do mundo da arte, trabalhando em diversos formatos, tais
                    como folhetos, tiras de jornal, revistas em quadrinhos, animação, charge, cartoon e caricatura,
                    manuais, cartuns, ilustrações, storyboards, pôsteres e videogames. O processo criativo de um
                    cartunista começa com um simples esboço a lápis, que é então aprimorado com a adição de tinta
                    nanquim ou similar, usando pincel ou caneta esferográfica. Essa última etapa do processo, conhecida
                    como arte final, também pode ser feita digitalmente, refletindo a evolução da tecnologia na arte.
