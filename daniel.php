                    <p>
                        Em 2013, expandiu seu universo criativo com o lançamento do primeiro volume da série "São Paulo dos Mortos", uma visão distópica de um futuro próximo dominado por zumbis.
                    </p>
                    <p>
                        Demonstrando sua versatilidade como autor, em 2019, ele presenteou os leitores com o romance gráfico "Último Assalto", ilustrado por Alex Rodrigues. Esta obra foi indicada ao prestigiado <strong>Prêmio Jabuti na categoria de melhor história em quadrinhos</strong> no ano seguinte.
                    </p>
                    <p>
                        Além de sua prolífica carreira como autor de quadrinhos, Daniel Esteves é também um empreendedor e educador dedicado. Ele fundou a editora independente Zapata Edições e criou o curso "HQ em Foco", ampliando seu impacto na cena cultural. Sua participação ativa nos coletivos Petisco e Quarto Mundo também destaca seu compromisso em fortalecer e enriquecer o cenário dos quadrinhos brasileiros.
